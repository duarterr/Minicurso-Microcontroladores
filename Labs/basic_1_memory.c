/* -------------------------------------------------------------------------------------------------------------------- */
// Includes
/* -------------------------------------------------------------------------------------------------------------------- */

#include <stdint.h>

/* -------------------------------------------------------------------------------------------------------------------- */
// Main function
/* -------------------------------------------------------------------------------------------------------------------- */

int main(void)
{
    volatile uint32_t ui32Loop;

    // Enable the GPIO port that is used for the on-board LED
    *((volatile uint32_t *)0x400FE108) = 0b00000000000000000000000000100000;

    // Delay for a bit
    for(ui32Loop = 0; ui32Loop < 200000; ui32Loop++);

    // Enable the GPIO pin for the LED (PF1). Set the direction as output, and enable the GPIO pin for digital function
    *((volatile uint32_t *)0x40025400) = 0b00000000000000000000000000000010;
    *((volatile uint32_t *)0x4002551C) = 0b00000000000000000000000000000010;

    // Loop forever
    while(1)
    {
        // Toggle the LED
        *((volatile uint32_t *)0x400253FC) ^= 0b00000000000000000000000000000010;

        // Delay for a bit
        for(ui32Loop = 0; ui32Loop < 200000; ui32Loop++);
    }
}

